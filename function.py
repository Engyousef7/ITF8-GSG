n1 = int(input("Enter n1"))
n2 = int(input("Enter n2"))

def add(n1,n2):
    add = n1+n2
    return add
def subtract(n1,n2):
    sub = n1+n2
    return sub
def multiplication(n1,n2):
    multi = n1 * n2
    return multi
def division(n1,n2):
    divi = n1/n2
    return divi
print(add(n1,n2))
print(subtract(n1,n2))
print(multiplication(n1,n2))
print(division(n1,n2))
